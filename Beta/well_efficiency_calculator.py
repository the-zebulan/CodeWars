def isefficient(x, y, threshold):
    total = 0
    for x2, y2 in DIRECTIONS:
        xx = x + x2
        yy = y + y2
        if 0 <= xx < 20 and 0 <= yy < 20:
            total += float(field[xx][yy])
    return total >= threshold


