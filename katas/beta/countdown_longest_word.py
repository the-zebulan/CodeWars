WORD_COUNTS = tuple(
    (a, Counter(a)) for a in sorted(words, key=lambda b: (-len(b), b))
)


def longest_word(letters):
    if not letters:
        return None
    letters_cnt = Counter(letters)
    max_len = 0
    result = []
    for word, word_cnt in WORD_COUNTS:
        if not word_cnt - letters_cnt:
            word_len = len(word)
            if word_len < max_len:
                break
            max_len = word_len
            result.append(word)
    return result or None
